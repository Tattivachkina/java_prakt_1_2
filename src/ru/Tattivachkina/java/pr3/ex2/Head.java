package ru.Tattivachkina.java.pr3.ex2;

public class Head {
    private String hair;
    private String eyes;

    public Head(String hair, String eyes) {
        this.hair = hair;
        this.eyes = eyes;
    }

    public String getHair() {
        return hair;
    }

    public String getEyes() {
        return eyes;
    }
}
